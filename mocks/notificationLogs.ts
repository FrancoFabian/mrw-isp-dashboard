    mockNotificationLogs
        .filter((l) => l.customerId === customerId)
        .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())

export const getLogsByStatus = (status: string) =>
    mockNotificationLogs.filter((l) => l.status === status)

export const getLogsByEventType = (eventType: string) =>
    mockNotificationLogs.filter((l) => l.eventType === eventType)
