/** Helper to generate checklist items from template */
function generateChecklistItems(
    templateId: string,
    status: "all_pending" | "in_progress" | "completed" | "partial"
): ChecklistItem[] {
    const template = mockChecklistTemplates.find((t) => t.id === templateId)
    if (!template) return []

    return template.items.map((item, index) => {
        let itemStatus: ChecklistItem["status"] = "pending"
        let completedAt: string | undefined
        let completedBy: string | undefined
        let completedByName: string | undefined

        if (status === "completed") {
            itemStatus = "completed"
            completedAt = "2026-02-05T11:30:00"
            completedBy = "STF-002"
            completedByName = "Luis Ramirez"
        } else if (status === "in_progress" && index < 3) {
            itemStatus = "completed"
            completedAt = "2026-02-06T10:30:00"
            completedBy = "STF-002"
            completedByName = "Luis Ramirez"
        } else if (status === "partial") {
            if (index < template.items.length - 2) {
                itemStatus = "completed"
                completedAt = "2026-02-05T14:00:00"
                completedBy = "STF-002"
                completedByName = "Luis Ramirez"
            }
        }

        return {
            id: `CHK-${templateId.split("-")[2]}-${index + 1}`,
            ...item,
            status: itemStatus,
            completedAt,
            completedBy,
            completedByName,
        }
    })
}

